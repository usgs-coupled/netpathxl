        if (j .eq. 1) then
            write(*,*) 'Phreeqc does not accept units of meq/L'
            stop
        endif        
        if (j .eq. 2) write(fileno, '(A15,a1)', advance='NO') 'mg/l', tab         
        if (j .eq. 3) write(fileno, '(A15,a1)', advance='NO') 'ppm', tab        
        if (j .eq. 4) write(fileno, '(A15,a1)', advance='NO') 'mmol/kgw', tab 
        
        !! redox
        indices(2) = 4;
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        j = 0
        if (val%VT == VT_R8) then
            j = val%VU%DOUBLE_VAL
        endif
        if (j .eq. 0) write(fileno, '("pe",a1)', advance='NO') tab 
        if (j .eq. 1) write(fileno, '("pe",a1)', advance='NO') tab   
        if (j .eq. 2) write(fileno, '("O(0)/O(-2)",a1)', advance='NO') tab 
        if (j .eq. 3) write(fileno, '("O(0)/O(-2)",a1)', advance='NO') tab  
        if (j .eq. 4) write(fileno, '("S(6)/S(-2)",a1)', advance='NO') tab  
            
        !! Temp
        indices(2) = 7;
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            f = val%VU%DOUBLE_VAL
            write(fileno, '(f15.3,a1)', advance='NO') f, tab
        else
            write(fileno, '(A15,a1)', advance='NO') " ", tab
        endif
        
        !! pH
        indices(2) = 8;
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            f = val%VU%DOUBLE_VAL
            write(fileno, '(f15.3,a1)', advance='NO') f, tab
        else
            write(fileno, '(A15,a1)', advance='NO') " ", tab
        endif
        
        !! pe
        indices(2) = 9;
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            f = val%VU%DOUBLE_VAL/.059
            write(fileno, '(f15.3,a1)', advance='NO') f, tab
        else
            write(fileno, '(A15,a1)', advance='NO') " ", tab
        endif
        
        !! Ca-SO4
        do j = 10, 15
            indices(2) = j;
            status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
            if (val%VT == VT_R8) then
                f = val%VU%DOUBLE_VAL
                write(fileno, '(f15.3,a1)', advance='NO') f, tab
            else
                write(fileno, '(A15,a1)', advance='NO') " ", tab
            endif
        enddo 
        
        !! Alkalinity and TDIC
        indices(2) = 5;
        j = 0
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))         
        if (val%VT == VT_R8) then
            j = val%VU%DOUBLE_VAL       
        endif
        
        !! j is alkalinity/tdic flag
        !! iunits is units flag
        
        indices(2) = 16;
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            f = val%VU%DOUBLE_VAL
            conv = 1.0
            if (iunits .eq. 2 .or. iunits .eq. 3) then
                if (j .lt. 3) then
                    conv = 50.04289 / 61.0173
                endif
            endif
            if (j .ne. 2) then
                !! Alkalinity defined, TDIC not defined
                write(fileno, '(f15.3,a1)', advance='NO') f * conv, tab           
                write(fileno, '(A15,a1)', advance='NO') " ", tab
            else
                !! Alkalinity not defined, TDIC defined
                write(fileno, '(A15,a1)', advance='NO') " ", tab
                write(fileno, '(f15.3,a1)', advance='NO') f * conv, tab           
            endif
        else
            write(fileno, '(A15,a1)', advance='NO') " ", tab           
            write(fileno, '(A15,a1)', advance='NO') " ", tab
        endif

        !! Fe - P
        do j = 17, 29
            indices(2) = j;
            status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
            if (val%VT == VT_R8) then
                f = val%VU%DOUBLE_VAL
                write(fileno, '(f15.3,a1)', advance='NO') f, tab
            else
                write(fileno, '(A15,a1)', advance='NO') " ", tab
            endif
        enddo 
        
        !! O2, no conversion necessary for mass, factor of 2 for moles
        indices(2) = 30;
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        conv = 1.0
        if (iunits .lt. 2 .or. iunits .gt. 3) conv = 2
        if (val%VT == VT_R8) then
            f = val%VU%DOUBLE_VAL
            write(fileno, '(f15.3,a1)', advance='NO') conv*f, tab
        else
            write(fileno, '(A15,a1)', advance='NO') " ", tab
        endif      
    
        !! H2S to CH4
        do j = 31, 33
            indices(2) = j;
            status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
            if (val%VT == VT_R8) then
                f = val%VU%DOUBLE_VAL
                write(fileno, '(f15.3,a1)', advance='NO') f, tab
            else
                write(fileno, '(A15,a1)', advance='NO') " ", tab
            endif
        enddo 
        
        !!
        !! 13C & 14C
        do j = 36, 37
            indices(2) = j;
            status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
            if (val%VT == VT_R8) then
                f = val%VU%DOUBLE_VAL
                write(fileno, '(f15.3,a1)', advance='NO') f, tab
            else
                write(fileno, '(A15,a1)', advance='NO') " ", tab
            endif
        enddo 
            
        !! 34S(6) and 34S(-2)
        any_present = .FALSE.
        do j = 38,  39
            indices(2) = j;
            l = j - 37
            present(l) = .FALSE.
            status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
            if (val%VT == VT_R8) then
                farray(l) = val%VU%DOUBLE_VAL
                f = farray(l)
                present(l) = .TRUE.
                any_present = .TRUE.
            endif
        enddo 
        if (any_present .eq. .FALSE.) then
            write(fileno, '(A15,a1)', advance='NO') " ", tab
            write(fileno, '(A15,a1)', advance='NO') " ", tab
        else 
            do j = 1, 2 
                if (present(j)) then
                    write(fileno, '(f15.3,a1)', advance='NO') farray(j), tab
                else
                    write(fileno, '(f15.3,a1)', advance='NO') f, tab
                endif 
            enddo
        endif        
        
        !! 2H through 87Sr
        do j = 40, 43
            indices(2) = j;
            status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
            if (val%VT == VT_R8) then
                f = val%VU%DOUBLE_VAL
                write(fileno, '(f15.3,a1)', advance='NO') f, tab
            else
                write(fileno, '(A15,a1)', advance='NO') " ", tab
            endif
        enddo 

        !! 15N for N(0), N(5), N(-3), N(3)
        any_present = .FALSE.
        present(4) = .FALSE.
        do j = 44, 46
            indices(2) = j;
            l = j - 43
            present(l) = .FALSE.
            status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
            if (val%VT == VT_R8) then
                farray(l) = val%VU%DOUBLE_VAL
                f = farray(l)
                present(l) = .TRUE.
                any_present = .TRUE.
            endif
        enddo 
        if (any_present .eq. .FALSE.) then
            write(fileno, '(A15,a1)', advance='NO') " ", tab
            write(fileno, '(A15,a1)', advance='NO') " ", tab
            write(fileno, '(A15,a1)', advance='NO') " ", tab
            write(fileno, '(A15,a1)', advance='NO') " ", tab
        else 
            do j = 1, 4 
                if (present(j)) then
                    write(fileno, '(f15.3,a1)', advance='NO') farray(j), tab
                else
                    write(fileno, '(f15.3,a1)', advance='NO') f, tab
                endif 
            enddo
        endif  
        
        !! Density
        indices(2) = 47;
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            f = val%VU%DOUBLE_VAL
            write(fileno, '(f15.3,a1)', advance='NO') f, tab
        else
            write(fileno, '(A15,a1)', advance='NO') " ", tab
        endif
        
        !! End of line
        write(fileno,'(a)')      
   
        !! increment n
        n = n + 1       
    enddo
    close(fileno)
    
    return
END SUBROUTINE xl2phreeqc
