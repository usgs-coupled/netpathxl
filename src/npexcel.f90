    integer location_num(45)
    !!
    TYPE (VARIANT) wnSafeArray  
    TYPE (VARIANT) flagsSafeArray  
    TYPE (VARIANT) val  
    TYPE(sa_bounds), DIMENSION(2) :: ab
    INTEGER iu_dup(MAXWELLS,4)
    CHARACTER*80 wllnms_dup(MAXWELLS)
    INTEGER*4, DIMENSION(2) :: indices
    INTEGER*4 lBound
    INTEGER*4 uBound

    data location/' G', ' H', &
	    'AD', ' P', 'AO', &
	    'AE', ' J', ' I', &
	    ' K', ' L', ' M', &
	    ' N', ' O', ' T', &
