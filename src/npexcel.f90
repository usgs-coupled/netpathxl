        stop 'File could not be opened'
    endif
        
 
    !! calculate range
    write(string, "(i5)") MAXWELLS + 8
    do i = 1, 4
    if (string(1:1) .eq. ' ') string = string(2:5)
    enddo
    string = "BI"//string

