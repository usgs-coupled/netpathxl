    USE IFQWIN
    USE IFCOM    
    USE ADOBJECTS
    implicit none
	TYPE (VARIANT) :: vInt
    integer*4 interior
    integer*4 status
    integer*4 color
    
    interior = range_GetInterior(rangeA0, status)
    CALL Check_Status(status, " Unable to get interior")
        ! pattern
    vInt%VT = VT_I4
