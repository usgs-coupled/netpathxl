	SUBROUTINE Check_Status(olestatus, errorMsg)
	  USE ADOBJECTS
	  IMPLICIT NONE                       

	  INTEGER*4 olestatus
	  CHARACTER (LEN = *) :: errorMsg

	  IF (olestatus >= 0) THEN
		  RETURN
	  END IF

	! Error handling code

	  WRITE (*, '(A, "; OLE error status = 0x", Z8.8, "; Aborting")') TRIM(errorMsg), olestatus
	  CALL SLEEPQQ(5000)
	  
	  CALL RELEASEOBJECTS()
	  CALL EXIT(-1)

	END SUBROUTINE
