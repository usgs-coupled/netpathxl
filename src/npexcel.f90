	    'AB', 'AC', 'AH', &
	    'AV', 'AU', 'AJ', &
	    'AK', 'AL', 'AM', &
	    'AN', 'AP', 'AG', &
	    'AQ', ' S', 'AF', &
	    'AR', 'AS', 'AT', &
	    'BB', 'BC', 'BD', &
	    'AI'/
    data location_num/7, 8, &  !  G,  H
        30, 16, 41, &          ! AD,  P, AO
        31, 10, 9,  &          ! AE,  J,  I
        11, 12, 13, &          !  K,  L,  M
        14, 15, 20, &          !  N,  O,  T
        21, 22, 23, &          !  U,  V,  W
        24, 25, 26, &          !  X,  Y,  Z
        17, 18, 27, &          !  Q,  R, AA
        28, 29, 34, &          ! AB, AC, AH
        48, 47, 36, &          ! AV, AU, AJ
        37, 38, 39, &          ! AK, AL, AM
        40, 42, 33, &          ! AN, AP, AG
        43, 19, 32, &          ! AQ,  S, AF
        44, 45, 46, &          ! AR, AS, AT
        54, 55, 56, &          ! BB, BC, BD
        35/                    ! AI

    WRITE (*,*) 'Reading Excel ...'
 
    !! calculate range
    write(string, "(i5)") MAXWELLS + 8
    do i = 1, 4
    if (string(1:1) .eq. ' ') string = string(2:5)
    enddo
    string = "BI"//string

    !! read data 
    call set_range("A8", string)
    wnSafeArray = Range_GetValue(range)  

    n = 1
    do i = 1, MAXWELLS 
       indices(1) = i;
       
       !! save well name B, column 2, cycle if empty
        indices(2) = 2;
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_BSTR) then
            status = ConvertBSTRToString(val%VU%PTR_VAL, string)
            wllnms(n)(5:80) = string
        else
            cycle
        endif
            
        !! Well num  A, column 1
        indices(2) = 1;
        tot(n) = n
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            tot(n) = val%VU%DOUBLE_VAL
        endif  

        !! save flags  C-F, columns 3-6
        do k=3,6
            indices(2) = k;
            status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
            if (val%VT == VT_R8) then
                iu(n, k - 2) = val%VU%DOUBLE_VAL
            endif    
        enddo

        !! save data values, columns defined in location_num
        do j = 1, 45
            indices(2) = location_num(j);
            status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
            if (val%VT == VT_R8) then
                dbdata(n,j) = val%VU%DOUBLE_VAL
                dbsfg(n, j) = 0
            else
                dbdata(n,j) = 0
                dbsfg(n, j) = -1
            endif
        enddo   
        
        !! save lat/lon values
        indices(2) = 52;   ! AZ
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            write(lat(n),"(G)") val%VU%DOUBLE_VAL
        else if (val%VT == VT_BSTR) then
            status = ConvertBSTRToString(val%VU%PTR_VAL, string)
            lat(n) = string
        endif        

		!       Address
        indices(2) = 57;   ! BE
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            write(address(n, 1),"(G)") val%VU%DOUBLE_VAL
        else if (val%VT == VT_BSTR) then
            status = ConvertBSTRToString(val%VU%PTR_VAL, string)
            address(n, 1) = string
        endif
        
        indices(2) = 58;   ! BF
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            write(address(n, 2),"(G)") val%VU%DOUBLE_VAL
        else if (val%VT == VT_BSTR) then
            status = ConvertBSTRToString(val%VU%PTR_VAL, string)
            address(n, 2) = string
        endif
        
        indices(2) = 59;   ! BG
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            write(address(n, 3),"(G)") val%VU%DOUBLE_VAL
        else if (val%VT == VT_BSTR) then
            status = ConvertBSTRToString(val%VU%PTR_VAL, string)
            address(n, 3) = string
        endif
        
        indices(2) = 60;   ! BH
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            write(address(n, 4),"(G)") val%VU%DOUBLE_VAL
        else if (val%VT == VT_BSTR) then
            status = ConvertBSTRToString(val%VU%PTR_VAL, string)
            address(n, 4) = string
        endif 
        
        indices(2) = 61;   ! BI
        status = SafeArrayGetElement(wnSafeArray%VU%PTR_VAL, indices(1), LOC(val))
        if (val%VT == VT_R8) then
            write(address(n, 5),"(G)") val%VU%DOUBLE_VAL
        else if (val%VT == VT_BSTR) then
            status = ConvertBSTRToString(val%VU%PTR_VAL, string)
            address(n, 5) = string
        endif  
        n = n + 1       
    enddo
    Nwlls = n - 1
    return
END SUBROUTINE xl2db
